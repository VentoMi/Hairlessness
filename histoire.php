    <section class="text-images section-photo margin-responsive history-img">
      <h3 class="h3-history">
        La santé
      </h3>
      <div>
          <p>Afin de pallier son manque de poils <span class="little-p">(photo 1)</span>, la peau du chat Sphynx est plus épaisse
          que celle des autres races de chats. De plus, on retrouve sous sa peau
          <b>une généreuse couche de graisse</b> qui le garde au chaud.
          Bien qu'épaisse, sa peau laisse souvent transparaître <b>ses veines palpitantes.</b>
          La peau du chat Sphynx respire, se salit, bronze et réagit comme la peau humaine.
          </p>
        </div>
        <div class="divMsg" id="divMsg3">
           <p>
            Ainsi, en été, il transpire pour évacuer la chaleur et peut attraper des coups de soleil.
            *L'absence de fourrure chez le chat Sphynx ne représente en aucun cas
            une tare génétique ou une maladie.</p>
          <p>
          Ce phénomène, qu'on appelle <b>alopécie héréditaire,</b>
          est dû à un gène qui a muté spontanément et de manière naturelle,
          et non à une manipulation génétique ou à un problème de santé.</p>
                <p>Au niveau des maladies à surveiller, <b>la cardiomyopathie hypertrophique féline</b> (hcm)
          est la principale chez le sphynx , comme pour 70% des chats de race.
          Contrairement aux légendes urbaines , l'HCM n'est pas l'apanage du sphynx .
          De ce fait, nous effectuons un <b>suivi échographique</b> tout particulier de nos reproducteurs
          que nous nous emmenons au CHU auprès du Dr Merveille (imminent cardiologue) et tous nos chats
          et nos chatons sont suivis par notre vétérinaire efficace et réactive le Dr Pascale DICKENSCHEID.</p>
                <p><b>La Cardiomyopathie hypertrophique</b> est caractérisée par un épaississement
          plus ou moins important de certaines parois du muscle cardiaque, appelé <b>hypertrophie</b>.
          La distribution de cet épaississement est variable d'un chat à l'autre.
          Cette hypertrophie touche principalement <b>le septum interventriculaire</b> (paroi entre le ventricule droit et le ventricule gauche) et la <b>paroi postérieure</b> libre du ventricule <span class="little-p">(photo 2)</span>
          (paroi externe du ventricule gauche), l’hypertrophie du côté droit n’est que très rarement rencontrée. Les muscles papillaires peuvent également être <b>hypertrophiés</b>.</p>
                <p>La CMH est la conséquence de la survenue d'anomalies sur les chromosomes
          responsables de la fabrication des différentes protéines constituant <b>le muscle cardiaque.</b> 
          Sa transmission reste <b>très difficile à anticiper</b> pour les éleveurs, car le gène responsable de la maladie est dominant, à pénétrance incomplète. <b>''Dominant''</b> c'est-à-dire que si l'un des deux parents est porteur du gène malade alors il le transmettra à sa , MAIS (c'est là que cela devient assez complexe) <b>''à pénétrance incomplète''</b> signifie que le chat peut être porteur sans déclarer la maladie, mais pourra le transmettre à sa descendance donc la transmission peut sauter une ou plusieurs génération(s)</p>
